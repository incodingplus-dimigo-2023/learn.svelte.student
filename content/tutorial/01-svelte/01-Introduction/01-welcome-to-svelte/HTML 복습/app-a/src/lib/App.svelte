Svelte는 다른 웹 프레임워크와 같이
<br>
<br>
Svelte는 웹 애플리케이션을 만들기 위한 도구입니다. 문법은 마치 HTML, CSS, Javascript를 합친 것과 같은 것처럼 느껴질 것입니다. 하지만 Svelte는 자신만의 문법으로 HTML, CSS, Javascript로 <strong>컴파일</strong>합니다. 그 결과를 웹 페이지에서 보여줄 것입니다.
<br>
<br>
Svelte를 쓰는 방법은 다양한데, 보통은 <strong>Sveltekit</strong>을 사용합니다. SvelteKit은 클라이언트와 서버를 섞어서 여러분이 쉽게 웹 개발을 할 수 있도록 해줍니다.
